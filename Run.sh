function stop() {
  echo "üõë Deteniendo procesos WEB-LIS PathSys..."
  
  # Detener contenedores Docker
  echo "  ‚Ä¢ Deteniendo contenedores Docker..."
  if [ -f "$DOCKER_COMPOSE_FILE" ]; then
    docker-compose -f "$DOCKER_COMPOSE_FILE" down 2>/dev/null || true
  fi
  
  # Detener procesos espec√≠ficos por puerto
  echo "  ‚Ä¢ Deteniendo Backend (puerto 8000)..."
  pkill -f "uvicorn.*8000" || true
  pkill -f "python3.*uvicorn" || true
  lsof -ti:8000 | xargs kill -9 2>/dev/null || true
  
  echo "  ‚Ä¢ Deteniendo Frontend (puerto 5174)..."
  pkill -f "npm run dev" || true
  pkill -f "vite" || true
  lsof -ti:5174 | xargs kill -9 2>/dev/null || true
  
  
  # Detener MongoDB local
  echo "  ‚Ä¢ Deteniendo MongoDB local..."
  brew services stop mongodb/brew/mongodb-community >/dev/null 2>&1 || true
  pkill -f mongod >/dev/null 2>&1 || true
  lsof -ti:27017 | xargs kill -9 2>/dev/null || true
  
  # Limpiar archivos de configuraci√≥n
  echo "  ‚Ä¢ Limpiando archivos de configuraci√≥n..."
  rm -f Back-End/.env Back-End/.env.example Back-End/.env.development Back-End/.env.production
  rm -f Front-End/.env Front-End/.env.development Front-End/.env.production Front-End/.env.local
  
  echo "‚úÖ Todos los procesos detenidos."
}

