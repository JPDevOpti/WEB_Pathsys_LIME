  echo "‚úÖ Back-End/.env creado para DOCKER"
  
  # Crear UN SOLO archivo .env para Front-End DOCKER
  echo "üîß Configurando Front-End DOCKER..."
  cat > Front-End/.env << EOF
VITE_API_BASE_URL=http://localhost:8000
VITE_APP_TITLE=WEB-LIS PathSys (Docker)
VITE_APP_ENV=development
VITE_DEV_MODE=true
EOF
  echo "‚úÖ Front-End/.env creado para DOCKER"
  
  # Verificar que Docker est√© corriendo
  if ! docker info >/dev/null 2>&1; then
    echo "‚ùå Docker no est√° corriendo. Por favor, inicia Docker Desktop."
    return 1
  fi
  
  # Verificar que el archivo docker-compose exista
  if [ ! -f "$DOCKER_COMPOSE_FILE" ]; then
    echo "‚ùå No se encontr√≥ $DOCKER_COMPOSE_FILE."
    echo "   Revisa que exista 'Back-End/docker-compose.dev.yml' o ajusta el script."
    return 1
  fi
  
  # Detener contenedores existentes
  echo "üõë Deteniendo contenedores existentes..."
  docker-compose -f "$DOCKER_COMPOSE_FILE" down 2>/dev/null || true
  
  # Construir e iniciar servicios
  echo "üî® Construyendo e iniciando servicios..."
  docker-compose -f "$DOCKER_COMPOSE_FILE" up --build -d
  
  echo "‚è≥ Esperando que los servicios est√©n listos..."
  sleep 10
  
  echo "‚úÖ Servicios Docker iniciados"
  echo "üìä MongoDB: mongodb://localhost:27017"
  echo "üîß API: http://localhost:8000"
  echo "üìñ API Docs: http://localhost:8000/docs"
  echo "üóÑÔ∏è  Mongo Express: http://localhost:8081"
  echo "üåê Frontend: http://localhost:5174"
}

