function setup() {
  echo "üîß Verificando dependencias del sistema..."
  
  # Verificar Docker
  if ! command -v docker &> /dev/null; then
    echo "‚ùå Docker no est√° instalado. Instalando..."
    brew install --cask docker
    echo "‚úÖ Docker instalado. Por favor, inicia Docker Desktop y ejecuta este script nuevamente."
    exit 1
  else
    echo "‚úÖ Docker ya est√° instalado"
  fi
  
  # Verificar Docker Compose
  if ! command -v docker-compose &> /dev/null; then
    echo "‚ùå Docker Compose no est√° instalado. Instalando..."
    brew install docker-compose
  else
    echo "‚úÖ Docker Compose ya est√° instalado"
  fi
  
  # Verificar Python 3.12
  if ! command -v /opt/homebrew/bin/python3.12 &> /dev/null; then
    echo "‚ùå Python 3.12 no est√° instalado. Instalando..."
    brew install python@3.12
  else
    echo "‚úÖ Python 3.12 ya est√° instalado"
  fi
  
  echo "üì¶ Instalando dependencias Front-End..."
  if cd Front-End && npm install --legacy-peer-deps || npm install --force; then
    cd ..
    echo "‚úÖ Dependencias Front-End instaladas"
  else
    echo "‚ùå Error instalando dependencias Front-End"
    exit 1
  fi
  
  echo "üêç Configurando entorno virtual para Back-End con Python 3.12..."
  if cd Back-End; then
    # Respaldar entorno virtual anterior si existe
    if [ -d "venv" ] && [ ! -d "venv_backup" ]; then
      echo "  ‚Ä¢ Respaldando entorno virtual anterior..."
      mv venv venv_backup_$(date +%Y%m%d_%H%M%S)
    fi
    
    if [ ! -d "venv" ]; then
      echo "  ‚Ä¢ Creando entorno virtual con Python 3.12..."
      /opt/homebrew/bin/python3.12 -m venv venv
    fi
    echo "  ‚Ä¢ Activando entorno virtual..."
    source venv/bin/activate
    echo "  ‚Ä¢ Actualizando pip..."
    pip install --upgrade pip
    echo "  ‚Ä¢ Instalando dependencias..."
    if [ -f requirements.txt ]; then
      pip install -r requirements.txt
    fi
    cd ..
    echo "‚úÖ Dependencias Back-End instaladas en entorno virtual con Python 3.12"
  else
    echo "‚ùå Error accediendo al directorio Back-End"
    exit 1
  fi
  
  
  echo "‚úÖ Configuraci√≥n completada"
}

function update_venv() {
  echo "üîÑ Actualizando entorno virtual a Python 3.12..."
  
  # Detener procesos de uvicorn si est√°n corriendo
  echo "‚èπÔ∏è  Deteniendo procesos de uvicorn..."
  pkill -f "uvicorn app.main:app" || true
  sleep 2
  
  # Respaldar el entorno virtual actual
  if [ -d "Back-End/venv" ]; then
    echo "üíæ Respaldando entorno virtual actual..."
    mv Back-End/venv Back-End/venv_backup_$(date +%Y%m%d_%H%M%S)
  fi
  
  # Crear nuevo entorno virtual con Python 3.12
  echo "üêç Creando nuevo entorno virtual con Python 3.12..."
  cd Back-End
  /opt/homebrew/bin/python3.12 -m venv venv
  
  # Activar el nuevo entorno
  echo "üîß Activando entorno virtual..."
  source venv/bin/activate
  
  # Actualizar pip
  echo "‚¨ÜÔ∏è  Actualizando pip..."
  pip install --upgrade pip
  
  # Instalar dependencias
  echo "üì¶ Instalando dependencias..."
  pip install -r requirements.txt
  
  cd ..
  echo "‚úÖ ¬°Entorno virtual actualizado exitosamente!"
  echo "üöÄ Para iniciar el servidor:"
  echo "   ./Run.sh local"
}

